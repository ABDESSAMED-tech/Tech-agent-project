    private void buyButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buyButtonActionPerformed
        // TODO add your handling code here:
        int selectedProduct = table.getSelectedRow();
        String pp = table.getValueAt(selectedProduct, 0).toString();
        AgentCentral.bought = pp;
        AgentCentral.qt = true;
        AgentCentral.qtBought = (int) spinner.getValue();
        try {
            Thread.sleep(2000);
        } catch (InterruptedException e1) {
            // TODO Auto-generated catch block
            e1.printStackTrace();
        }

        if (table.getValueAt(selectedProduct, 3).toString().equals("Magasin 1")) {
            table.setValueAt(data.infoMagasin1.get(pp).quantity, selectedProduct, 2);

            if (data.infoMagasin1.get(pp).quantity == 0) {
                tableModel.removeRow(selectedProduct);
            }
        }

        if (table.getValueAt(selectedProduct, 3).toString().equals("Magasin 2")) {
            table.setValueAt(data.infoMagasin2.get(pp).quantity, selectedProduct, 2);

            if (data.infoMagasin2.get(pp).quantity == 0) {
                tableModel.removeRow(selectedProduct);
            }
        }

        if (table.getValueAt(selectedProduct, 3).toString().equals("Magasin 3")) {
            table.setValueAt(data.infoMagasin3.get(pp).quantity, selectedProduct, 2);

            if (data.infoMagasin3.get(pp).quantity == 0) {
                tableModel.removeRow(selectedProduct);
            }
        }

    }//GEN-LAST:event_buyButtonActionPerformed
